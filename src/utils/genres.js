/**
 * @description - Get full years from the current year to 2000
 * @returns {Array}
 */
function getFullYearRange() {
  const currentYear = new Date().getFullYear(); // Get the current year
  const years = [];

  for (let year = currentYear; year >= 2000; year--) {
    years.push(year);
  }

  return years;
}