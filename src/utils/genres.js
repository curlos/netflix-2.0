export const getGenreNames = (genreIDs, mediaType) => {
  const genreNames = [];

  if (genreIDs) {
    for (let genreID of genreIDs) {
      const genreName = getGenreName(genreID, mediaType);
      if (genreName) {
        genreNames.push(genreName);
      }
    }
  }

  return genreNames;
};

const getGenreName = (genreID) => {
  for (let genre of GENRES) {
    if (genre.id === genreID) {
      return genre.name;
    }
  }
};

